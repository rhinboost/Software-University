using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _5.Boolean_Variable
{
    class Program
    {
        static void Main(string[] args)
        {


            string str = Console.ReadLine();

            if (str == "True")
            {
                Console.WriteLine("Yes");
            }
            else if (str == "False")
            {
                Console.WriteLine("No");

            }
        }
    }
}
